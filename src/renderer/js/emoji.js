// Replace emoji shortcodes with actual emojis
function replaceEmojis(text) {
  return text.replace(/:([a-zA-Z0-9_+-]+):/g, (match, code) => {
    const emoji = emojiMap[code.toLowerCase()];
    return emoji || match;
  });
}

